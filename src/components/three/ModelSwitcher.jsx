import { PresentationControls } from "@react-three/drei";
import React, { useRef } from "react";
import MacbookModel16 from "../models/Macbook-16.jsx";

import MacbookModel14 from "../models/Macbook-14.jsx";
import gsap from "gsap";
import { useGSAP } from "@gsap/react";

const Animation_Duration = 1;
const Animation_Offset_distance = 7;

const fadeMeshes = (group, opacity) => {
  if (!group) return;

  group.traverse((child) => {
    if (child.isMesh) {
      child.material.transparent = true;
      gsap.to(child.material, { opacity, duration: Animation_Duration });
    }
  });
};
const moveGroup = (group, x) => {
  if (!group) return;
  gsap.to(group.position, { x, duration: Animation_Duration });
};

const ModelSwitcher = ({ scale, isMobile }) => {
  const Scale_large = 0.08;
  const Scale_small = 0.05;
  const smallMacbookRef = useRef();
  const largeMacbookRef = useRef();

  const showLargeMacbook = scale === Scale_large || scale === Scale_small;
  useGSAP(() => {
    if (showLargeMacbook) {
      moveGroup(smallMacbookRef.current, -Animation_Offset_distance);
      moveGroup(largeMacbookRef.current, 0);

      fadeMeshes(smallMacbookRef.current, 0);
      fadeMeshes(largeMacbookRef.current, 1);
    } else {
      moveGroup(smallMacbookRef.current, 0);
      moveGroup(largeMacbookRef.current, Animation_Offset_distance);

      fadeMeshes(smallMacbookRef.current, 1);
      fadeMeshes(largeMacbookRef.current, 0);
    }
  }, [scale]);
  const controlsConfig = {
    snap: true,
    speed: 1,
    zoom: 1,
    azimuth: [-Infinity, Infinity],
    config: { mass: 1, tension: 0, friction: 26 },
  };
  return (
    <>
      <PresentationControls {...controlsConfig}>
        <group ref={largeMacbookRef}>
          <MacbookModel16 scale={isMobile ? 0.05 : 0.08} />
        </group>

        <group ref={smallMacbookRef}>
          <MacbookModel14 scale={isMobile ? 0.03 : 0.06} />
        </group>
      </PresentationControls>
    </>
  );
};
export default ModelSwitcher;
